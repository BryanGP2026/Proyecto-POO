// Método para llenar la tabla con los datos del Gestor
    private void actualizarTabla() {
        // Obtenemos el modelo de la tabla visual
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblProductos.getModel();
        
        // Limpiamos la tabla primero para que no se dupliquen visualmente los datos
        modeloTabla.setRowCount(0); 

        // Llamamos al método listar() que usa los Streams de tu rúbrica
        java.util.List<modelo.Producto> lista = gestor.listar();

        // Recorremos la lista y agregamos cada producto como una fila nueva
        for (modelo.Producto p : lista) {
            Object[] fila = {
                p.getCodigoBarras(), 
                p.getNombre(), 
                p.getPrecio(), 
                p.getStock()
            };
            modeloTabla.addRow(fila);
        }
    }
    
