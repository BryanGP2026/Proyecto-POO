    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
        // 1. Extraemos el texto que el usuario escribió en las CAJAS EN BLANCO
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        // Convertimos los textos a números porque la base de datos lo exige
        double precio = Double.parseDouble(txtPrecio.getText());
        int stock = Integer.parseInt(txtStock.getText());

        // 2. Creamos el objeto Producto usando el modelo
        modelo.Producto nuevoProducto = new modelo.Producto(codigo, nombre, precio, stock);

        // 3. Le decimos al gestor que lo guarde en el HashMap y en MySQL
        if (gestor.registrar(nuevoProducto)) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Producto registrado con éxito!");
            
            // Limpiamos las cajas para registrar uno nuevo
            txtCodigo.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtStock.setText("");
            
            actualizarTabla();
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al registrar. Revisa si el código ya existe.");
        }
    } catch (NumberFormatException e) {
        // Validación por si el usuario escribe letras en el precio o stock
        javax.swing.JOptionPane.showMessageDialog(this, "Error: El precio y stock deben ser números.");
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        // Obtenemos la fila que el usuario seleccionó con el mouse
    int filaSeleccionada = tblProductos.getSelectedRow();
    
    if (filaSeleccionada >= 0) {
        // Pasamos los datos de la tabla a las cajas de texto
        txtCodigo.setText(tblProductos.getValueAt(filaSeleccionada, 0).toString());
        txtNombre.setText(tblProductos.getValueAt(filaSeleccionada, 1).toString());
        txtPrecio.setText(tblProductos.getValueAt(filaSeleccionada, 2).toString());
        txtStock.setText(tblProductos.getValueAt(filaSeleccionada, 3).toString());
        
        // Desactivamos la caja del código para que no lo puedan cambiar (es la llave primaria)
        txtCodigo.setEnabled(false); 
    }
    }//GEN-LAST:event_tblProductosMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int stock = Integer.parseInt(txtStock.getText());

        modelo.Producto productoModificado = new modelo.Producto(codigo, nombre, precio, stock);

        if (gestor.modificar(productoModificado)) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Producto modificado correctamente!");
            actualizarTabla(); // Refresca la tabla
            
            // Limpiamos y volvemos a activar la caja del código
            txtCodigo.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtStock.setText("");
            txtCodigo.setEnabled(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar.");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Revisa los números ingresados.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String codigo = txtCodigo.getText();
    
    if (!codigo.isEmpty()) {
        // Pedimos confirmación antes de borrar
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, 
                "¿Estás seguro de eliminar este producto?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
                
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            if (gestor.eliminar(codigo)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado.");
                actualizarTabla(); // Refresca la tabla
                
                // Limpiamos y activamos la caja del código
                txtCodigo.setText("");
                txtNombre.setText("");
                txtPrecio.setText("");
                txtStock.setText("");
                txtCodigo.setEnabled(true);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar.");
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un producto de la tabla primero.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

